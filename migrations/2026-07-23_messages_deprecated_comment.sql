-- Помечаем public.messages как похороненную таблицу — данные оставляем на месте.
--
-- Раздел «Коммуникации» удалён из платформы 2026-07-23 (CommunicationsView,
-- роут, пункты сайдбара, весь chat-API в dataService). Чат внутри платформы не
-- нужен: общение Лиги живёт в Телеграме. Таблица при этом и не работала —
-- у роли authenticated нет ни одного гранта и ни одной PERMISSIVE-политики,
-- то есть SELECT/INSERT/UPDATE/DELETE отдавали жёсткий 403.
--
-- Зачем комментарий: без него таблица будет всплывать в каждом следующем
-- RLS-аудите как «ноль permissive-политик» и каждый раз требовать разбора.
-- Комментарий говорит будущему аудитору (и будущему мне), что это осознанно
-- закрытая таблица, а не дыра, которую надо чинить.
--
-- Дроп — отдельным решением, когда накопится схемный мусор и соберём общую
-- cleanup-миграцию. Сейчас: 4 тестовых сообщения от 2026-03-17, 64 kB,
-- ноль FK в обе стороны, ноль триггеров, ноль зависимых вью.

COMMENT ON TABLE public.messages IS
  'DEPRECATED 2026-07-23: раздел «Коммуникации» удалён из платформы, общение Лиги живёт в Телеграме. Данные (4 тестовых сообщения от 2026-03-17) оставлены намеренно. К таблице не обращается ни фронт, ни бэкенд. Отсутствие permissive RLS-политик и грантов у authenticated — осознанно, а не дыра: не заводить их и не «чинить» при аудитах. Дроп — отдельным решением в общей cleanup-миграции.';

-- ── Верификация ──────────────────────────────────────────────────────────────
\echo === Комментарий на public.messages (ожидание: текст DEPRECATED…) ===
SELECT obj_description('public.messages'::regclass, 'pg_class') AS table_comment;

\echo === Данные на месте (ожидание: 4 строки) ===
SELECT count(*) AS rows FROM public.messages;
